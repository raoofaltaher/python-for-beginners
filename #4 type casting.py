#type casting is convert the data type of a value to another data type

x = 1 #int
y = 2.0 #float
z = "3" #str

print(x)
print(y)
print(z)

x = float(x)
y = float(y)
z = float(z)

print(x)
print(y)
print(z)


x = int(x)
y = int(y)
z = int(z)

print(x)
print(y)
print(z)

x = str(x)
y = str(y)
z = str(z)

print("X value:", x)
print("Y value:", y)
print("Z value:", z)
